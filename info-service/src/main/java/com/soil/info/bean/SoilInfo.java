package com.soil.info.bean;

public class SoilInfo {

    private int id;

    private String name;

    private float th;

    private float tl;

    private float hh;

    private float hl;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getTh() {
        return th;
    }

    public void setTh(float th) {
        this.th = th;
    }

    public float getTl() {
        return tl;
    }

    public void setTl(float tl) {
        this.tl = tl;
    }

    public float getHh() {
        return hh;
    }

    public void setHh(float hh) {
        this.hh = hh;
    }

    public float getHl() {
        return hl;
    }

    public void setHl(float hl) {
        this.hl = hl;
    }
}
